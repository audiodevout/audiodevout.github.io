/**
 * Bundles data/*.json into js/portfolioData.parts.js for file:// preview.
 * Also bundles print portfolio data into portfolio/portfolioData.parts.js.
 *
 * After CMS edits, run the full build before pushing:
 *   npm run build
 *
 * Local preview (recommended):
 *   npm run dev
 *   then open http://localhost:8080/
 *
 * That regenerates portfolioData.parts.js, work/<id>/ pages, sitemap.xml, and robots.txt.
 */
"use strict";

var fs = require("fs");
var path = require("path");

var root = path.join(__dirname, "..");
var dataDir = path.join(root, "data");
var outFile = path.join(root, "js", "portfolioData.parts.js");
var printOutFile = path.join(root, "portfolio", "portfolioData.parts.js");

var files = {
  sound: "sound.json",
  performance: "performance.json",
  installations: "installations.json",
  visual: "visual.json",
  writing: "writing.json",
  funProjects: "fun-projects.json",
  exhibitions: "exhibitions.json",
  links: "links.json",
  profile: "profile.json",
};

var parts = {};
Object.keys(files).forEach(function (key) {
  var filePath = path.join(dataDir, files[key]);
  parts[key] = JSON.parse(fs.readFileSync(filePath, "utf8"));
});

var output =
  "/** Generated by scripts/build-portfolio-data.js — do not edit by hand */\n" +
  "window.__PORTFOLIO_PARTS__ = " +
  JSON.stringify(parts) +
  ";\n";

fs.writeFileSync(outFile, output, "utf8");
console.log("Wrote " + path.relative(root, outFile));

var printConfigPath = path.join(root, "portfolio", "config.json");
var printParts = {
  config: JSON.parse(fs.readFileSync(printConfigPath, "utf8")),
  profile: parts.profile,
  links: parts.links,
  installations: parts.installations,
  performance: parts.performance,
  visual: parts.visual,
  exhibitions: parts.exhibitions,
};

var printOutput =
  "/** Generated by scripts/build-portfolio-data.js — do not edit by hand */\n" +
  "window.__PORTFOLIO_PRINT_PARTS__ = " +
  JSON.stringify(printParts) +
  ";\n";

fs.writeFileSync(printOutFile, printOutput, "utf8");
console.log("Wrote " + path.relative(root, printOutFile));
